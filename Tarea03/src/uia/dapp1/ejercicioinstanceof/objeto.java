package uia.dapp1.ejercicioinstanceof;

public class objeto {
    private float lado;
    private float base;

    public objeto(float lado, float base) {
        this.lado = lado;
        this.base = base;
    }

    public float getLado() {
        return lado;
    }

    public void setLado(float lado) {
        this.lado = lado;
    }

    public float getBase() {
        return base;
    }

    public void setBase(float base) {
        this.base = base;
    }
}
